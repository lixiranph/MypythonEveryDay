'''
列表元素的增加
'''
a=[20,40]
a.append(80)
print(a)
#+运算符
a=[20,40]
print(id(a))
a=a+[50]
print(id(a))
print(a)
#extend方法
a=[20,40]
print(id(a))
a.extend([50])
print(id(a))
print(a)
#insert方法
a=[10,20,30]
a.insert(1,50)
print(a)
#乘法扩展
a=['lxr',100]
b=a*3
print(a)
print(b)

'''
列表元素的删除
del
'''
a=[10,20,30]
del a[1]
print(a)
#pop方法 #删除指定位置的元素，未指定默认最后
a=[10,20,30,40,50]
a.pop()
print(a)
a.pop(1)
print(a)
'''
remove方法删除制定位置的元素，若不存在该元素抛出异常
'''
a=[10,20,30,40,50,20,30,20,30]
a.remove(20)
print(a)
a.remove(100)
print(a)