import turtle
turtle.showturtle() #显示箭头
turtle.width(10)
turtle.color('blue')#画笔颜色改为红色
turtle.circle(50)
turtle.penup()#抬笔
turtle.goto(120,0)
turtle.color('black')
turtle.pendown()#下笔
turtle.circle(50)
turtle.penup()#抬笔
turtle.goto(240,0)
turtle.color('red')
turtle.pendown()#下笔
turtle.circle(50)
turtle.penup()#抬笔
turtle.goto(60,-50)
turtle.color('yellow')
turtle.pendown()#下笔
turtle.circle(50)
turtle.penup()#抬笔
turtle.goto(180,-50)
turtle.color('green')
turtle.pendown()#下笔
turtle.circle(50)
